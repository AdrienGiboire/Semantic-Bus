'use strict';

var mongoose = require('mongoose');

// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------



var UpdatePasswordSchema = mongoose.Schema({

  timeStamp: {
    type: Date,
    default: Date.now,
    required: true
  },
  userMail: {
    type: String,
    required: true
  },
  token: {
    type: String,
    required: true
  },
});
// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------

module.exports = UpdatePasswordSchema;
